#define OLED_I2C_ADDR 0x78      //OLED地址
#define OLED_TIMEOUT  500       //超时时间

extern I2C_HandleTypeDef hi2c1;
static I2C_HandleTypeDef *I2C_Dataout = &hi2c1;            //发送的IIC
