/*
 *  函数名：Oled_Test
 *  功能描述：I2C发送测试命令给OLED
 *  输入参数：无
 *  输出参数：无
 *  返回值：  HAL_OK
             HAL_ERROR
             HAL_BUSY
             HAL_TIMEOUT
 */
HAL_StatusTypeDef OLED_Test(void)
{
    return HAL_I2C_IsDeviceReady(I2C_Dataout, OLED_I2C_ADDR,1,OLED_TIMEOUT);
}
/*
 *  函数名：OLED_WriteCmd
 *  功能描述：I2C发送命令给OLED
 *  输入参数：chr->要发送的命令字符串
 *          len->字符串长度
 *  输出参数：无
 *  返回值：0-成功, 其他值失败
 */
uint8_t OLED_WriteCmd(uint8_t *chr,uint8_t len)
{
    return HAL_I2C_Mem_Write(I2C_Dataout,OLED_I2C_ADDR,0x00,I2C_MEMADD_SIZE_8BIT,(uint8_t *) &chr[0],len,OLED_TIMEOUT);
}
/*
 *  函数名：OLED_WriteData
 *  功能描述：I2C发送命令给OLED
 *  输入参数：chr->要发送的数据字符串
 *          len->字符串长度
 *  输出参数：无
 *  返回值：0-成功, 其他值失败
 */
uint8_t OLED_WriteData(uint8_t *chr,uint8_t len)
{
    return HAL_I2C_Mem_Write(I2C_Dataout,OLED_I2C_ADDR,0x40,I2C_MEMADD_SIZE_8BIT,(uint8_t *) &chr[0],len,OLED_TIMEOUT);
}
/*
 *  函数名：Oled_Init
 *  功能描述：OLED初始化
 *  输入参数：无
 *  输出参数：无
 *  返回值：无
 */
void OLED_Init(void)
{
    HAL_Delay(200);

    // 参考代码的页地址模式初始化
    uint8_t init_cmds[] = {
        0xAE,       // 关闭显示

        // 时钟和显示设置
        0xD5, 0x80,  // 设置显示时钟分频比/振荡器频率
        0xA8, 0x3F,  // 设置多路复用率 (64-1=63=0x3F)
        0xD3, 0x00,  // 设置显示偏移
        0x40,        // 设置显示起始行

        // 地址模式：页地址模式
        0x20, 0x02,  // ← 关键！改为页地址模式

        // 显示方向
        0xA1,        // 设置段重映射 - 水平翻转
        0xC8,        // 设置COM扫描方向 - 垂直翻转

        // 硬件配置
        0xDA, 0x12,  // 设置COM硬件引脚配置
        0x81, 0xCF,  // 设置对比度控制

        // 时序设置
        0xD9, 0xF1,  // 设置预充电周期
        0xDB, 0x30,  // 设置VCOMH取消选择电平

        // 显示控制
        0xA4,        // 关闭整体显示
        0xA6,        // 设置正常显示模式

        // 电荷泵设置（关键）
        0x8D, 0x14,  // 使能电荷泵

        // 重新配置COM引脚（电荷泵使能后）
        0xDA, 0x12,

        // 开启显示
        0xAF,
    };

    OLED_WriteCmd(init_cmds, sizeof(init_cmds));
    HAL_Delay(100);
    OLED_Clear();
}
/*
 *  函数名：OLED_SetCursor
 *  功能描述：OLED输出光标位置
 *  输入参数：x->光标横向位置(0 ~ 128)
 *           y->光标横向位置(0 ~ 8)
 *  输出参数：无
 *  返回值：无
 */
void OLED_SetCursor(uint8_t x, uint8_t Page)
{
    // 按照正确的顺序：先设置页，再设置列
    // 页地址命令：0xB0 + Page (0~7)
    HAL_I2C_Mem_Write(I2C_Dataout, OLED_I2C_ADDR, 0x00,
                     I2C_MEMADD_SIZE_8BIT, (uint8_t[]){0xB0 | (Page & 0x07)}, 1, 100);

    // 列地址低4位：0x00 + (x & 0x0F)
    HAL_I2C_Mem_Write(I2C_Dataout, OLED_I2C_ADDR, 0x00,
                     I2C_MEMADD_SIZE_8BIT, (uint8_t[]){0x00 | (x & 0x0F)}, 1, 100);

    // 列地址高4位：0x10 + ((x >> 4) & 0x0F)
    HAL_I2C_Mem_Write(I2C_Dataout, OLED_I2C_ADDR, 0x00,
                     I2C_MEMADD_SIZE_8BIT, (uint8_t[]){0x10 | ((x >> 4) & 0x0F)}, 1, 100);
}
/*
 *  函数名：OLED_Clear
 *  功能描述：OLED清屏
 *  输入参数：无
 *  输出参数：无
 *  返回值：无
 */
void OLED_Clear(void)
{
    for (uint8_t j = 0; j < 8; j++)
    {
        OLED_SetCursor(0,j);
        for (uint8_t i = 0; i < 128; i++)
        {
            OLED_DisplayBuf[j][i] = 0x00;
        }
    }
    HAL_Delay(100);
}
/*
 *  函数名：OLED_ClearArea
 *  功能描述：OLED区域清屏
 *  输入参数： x-> 清屏开始x轴
 *            y-> 清屏开始y轴
 *            width->清屏宽度
 *            Height->清屏高度
 *  输出参数：无
 *  返回值：无
 */
void OLED_ClearArea(uint8_t x, uint8_t y, uint8_t width,uint8_t Height)
{
    for (uint8_t j = y; j < y + Height; j++)
    {
        for (uint8_t i = x; i < x + width; i++)
        {
            OLED_DisplayBuf[j / 8][i] &= ~(0x01 << (j % 8));
        }

    }
}
/*
 *  函数名：OLED_ShowChar
 *  功能描述：OLED显示字符
 *  输入参数： x-> 光标x轴（0 ~ 15）
 *            y-> 光标y轴（0 ~ 7）
 *            chr-> 显示的字符
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t chr)
{
    OLED_ShowImage(x * 8,y * 16,8,16,(uint8_t *)ascii_font[chr]);
}
/*
 *  函数名：OLED_ShowString
 *  功能描述：OLED显示字符串
 *  输入参数： x-> 光标x轴（0 ~ 15）
 *            y-> 光标y轴（0 ~ 7）
 *            chr-> 显示的字符串
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowString(uint8_t x, uint8_t y, uint8_t *chr)
{
    for (uint8_t i = 0; chr[i] != '\0'; i++)
    {
        OLED_ShowChar(x + i, y, chr[i]);
    }
}
/*
 *  函数名：OLED_ShowNum
 *  功能描述：OLED显示数字(10进制)
 *  输入参数： x-> 光标x轴（0 ~ 15）
 *            y-> 光标y轴（0 ~ 7）
 *            Number-> 显示的数字
 *            Length-> 显示的数字长度
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t Number, uint8_t Length)
{
    for (uint8_t i = 0; i < Length; i++)
    {
        OLED_ShowChar(x + Length - i - 1, y, Number / OLED_Numexponent(10,i) % 10 + '0');
    }
}
/*
 *  函数名：OLED_ShowHexNum
 *  功能描述：OLED显示数字(16进制)
 *  输入参数： x-> 光标x轴（0 ~ 15）
 *            y-> 光标y轴（0 ~ 7）
 *            Number-> 显示的数字
 *            Length-> 显示的数字长度
 *            mode-> 显示0x前缀（0 or 1）
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowHexNum(uint8_t x, uint8_t y, uint32_t Number, uint8_t Length, uint8_t mode)
{
    uint8_t Num, Placeholder = 0;
    if ( mode == 'y')
    {
        Placeholder = 2 ;
        OLED_ShowString(x,y,"0x");
    }
    for (uint8_t i = 0; i < Length; i++)
    {
        Num = Number / OLED_Numexponent(16,i) % 16;
        if (Num < 10)
        {
            OLED_ShowChar(x + Length - i - 1 + Placeholder, y, Num + '0');
        }
        else
        {
            OLED_ShowChar(x + Length - i - 1 + Placeholder, y, Num + 'A');
        }
    }
}
/*
 *  函数名：OLED_ShowSignedNum
 *  功能描述：OLED显示数字(16进制)
 *  输入参数： x-> 光标x轴（0 ~ 15）
 *            y-> 光标y轴（0 ~ 7）
 *            Number-> 显示的数字
 *            Length-> 显示的数字长度
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowSignedNum(uint8_t x, uint8_t y, int32_t Number, uint8_t Length)
{
    uint8_t i;
    uint32_t Number1;
    if (Number >= 0)
    {
        OLED_ShowChar(x, y, '+');
        Number1 = Number;
    }
    else
    {
        OLED_ShowChar(x, y, '-');
        Number1 = -Number;
    }
    for (i = 0; i < Length; i++)
    {
        OLED_ShowChar(x + Length - i , y, Number1 / OLED_Numexponent(10,i) % 10 + '0');
    }
}

/*
 *  函数名：OLED_Numexponent
 *  功能描述：求次方数
*  输入参数： base-> 底数
 *          frequency-> 次数
 *  输出参数：无
 *  返回值：无
 */
uint32_t OLED_Numexponent(uint8_t base, uint8_t frequency)
{
    uint32_t Num = 1;
    for (uint8_t i = 0; i < frequency; i++)
    {
        Num *= base;
    }
    return Num;
}
/*
 *  函数名：OLED_UpData
 *  功能描述：OLED更新显存
 *  输入参数：无
 *  输出参数：无
 *  返回值：无
 */
void OLED_UpData(void)
{
   for (uint8_t j = 0; j < 8; j ++)
   {
       OLED_SetCursor(0,j);
       OLED_WriteData((uint8_t *) &OLED_DisplayBuf[j],128);
   }

}
/*
 *  函数名：OLED_ShowImage
 *  功能描述：OLED显示图形
 *  输入参数： x-> 光标x轴(0 ~ 127)
 *            y-> 光标y轴(0 ~ 63)
 *            width-> 显示的宽度
 *            Height-> 显示的高度
 *            buff-> 显示的图像字符串
 *  输出参数：无
 *  返回值：无
 */
void OLED_ShowImage(uint8_t x, uint8_t y, uint8_t width,uint8_t Height,uint8_t *buff)
{
    if (x < 127 && y < 64)
    {
        OLED_ClearArea(x,y,width,Height);

        for (uint8_t j = 0; j < (Height - 1) / 8 + 1; j++)
        {
            for (uint8_t i = 0; i < width; i++)
            {
                OLED_DisplayBuf[y / 8 + j][x + i] |= buff[j * width + i] << (y % 8);
                OLED_DisplayBuf[y / 8 + j + 1 ][x + i] |= buff[j * width + i] >> (8 - y % 8);
            }
        }
    }
}
/*
 *  函数名：OLED_DrawPoint
 *  功能描述：OLED显示像素点
*  输入参数： x-> 光标x轴(0 ~ 127)
 *            y-> 光标y轴(0 ~ 63)
 *  输出参数：无
 *  返回值：无
 */
void OLED_DrawPoint(uint8_t x, uint8_t y)
{
    if (x < 128 && y < 64)
    {
        OLED_DisplayBuf[y / 8][x] |= 0x01 << (y % 8);
    }

}
/*
 *  函数名：OLED_DrawPoint
 *  功能描述：OLED像素点读取
*  输入参数： x-> 光标x轴(0 ~ 127)
 *            y-> 光标y轴(0 ~ 63)
 *  输出参数：1->有像素
 *           0->无像素
 *  返回值：无
 */
uint8_t OLED_GetPoint(uint8_t x, uint8_t y)
{
    if (x < 128 && y < 64)
    {
        if ( OLED_DisplayBuf[y / 8][x] & 0x01 << (y % 8))
        {
            return 1;
        }
    }
    return 0;
}

